    double circumference3 = 0,
    double circumference2Calc = 0,
    double circumference1Calc = 0,
    double o3 = 0,
    double o2 = 0,
    double o1 = 0,
  }) : super(
    id: id,
    name: name,
    image: image,
    uom: uom,
    remarks: remarks,
    displayOrder: displayOrder,
    cardFormState: cardFormState,
    materialCode: materialCode,
    fieldValues: fieldValues,
    customLabels: customLabels,
    qty: qty,
