      layer2Area: layer2Area ?? this.layer2Area,
      layer3Area: layer3Area ?? this.layer3Area,
      circumference3: circumference3 ?? this.circumference3,
      circumference2Calc: circumference2Calc ?? this.circumference2Calc,
      circumference1Calc: circumference1Calc ?? this.circumference1Calc,
